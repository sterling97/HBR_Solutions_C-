// 8) Crea un programa que escriba en pantalla los números del 1 al 10, usando "while".


using System;

namespace escribir_numeros_while_8
{
    class Program
    {
        static void Main(string[] args)
        {
            int count;

            count = 0;

            while (count < 10)
            {
                count += 1;
                Console.Write(count);
            }











        }
    }
}
